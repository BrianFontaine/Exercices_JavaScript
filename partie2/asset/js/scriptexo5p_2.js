var day = parseInt(prompt('Choisissez un Nombre (1 Ã  7) :'));

switch (day) {
    case 1:
        window.alert('Lundi');
    break;

    case 2:
        window.alert('Mardi.');
    break;

    case 3:
        window.alert('Mercredi');
    break;

    case 4:
        window.alert('Jeudi');
    break;

    case 5:
        window.alert('Vendredi');
    break;

    case 6:
        window.alert('Samedi');
    break;

    case 7:
        window.alert('Dimanche');
    break;
}